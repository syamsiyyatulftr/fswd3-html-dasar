//  Relasi/Perbandingan/Comparison
let n = 20;
let z = 5;

console.log(n == z);    // Sama dengan
console.log(n < z);     // Lebih kecil
console.log(n > z);     // Lebih besar
console.log(n != z);    // Tidak sama dengan
console.log(n >= z);    // Lebih besar sama dengan
console.log(n <= z);    // Lebih kecil sama dengan

//  Logika/Logical
let o = true;
let r = false;

console.log(o && r);    // Logika AND
console.log(o || r);    // Logia OR
console.log(!o);        // Negasi/kebalikan

//  Bitwise/biner
let v = 00111100;
let t = 00001101;

console.log(v & t);     // AND
console.log(v | t);     // OR
console.log(v ^ t);     // XOR
console.log(~ v);       // Negasi/kebalikan
console.log(v << t);    // Left Shift
console.log(v >> t);    // Right Shift


